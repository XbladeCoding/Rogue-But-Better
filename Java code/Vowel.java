public class Vowel {
  public static void main(String[] args) {
    String letter = args[0];
    switch (letter) {
      case "a":
        System.out.println("It's a vowel!");
        break;
      case "e":
        System.out.println("It's a vowel!");
        break;
      case "i":
        System.out.println("It's a vowel!");
        break;
      case "o":
        System.out.println("It's a vowel!");
        break;
      case "u":
        System.out.println("It's a vowel!");
        break;
      default:
        System.out.println("It's a consonant!");
        break;
    }
  }
}